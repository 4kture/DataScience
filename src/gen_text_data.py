import pandas as pd
import random

topics = [
    "Образование", "Здравоохранение", "Транспорт", "Экология", "Благоустройство",
    "Безопасность", "ЖКХ", "Культура", "Спорт", "Социальная поддержка"
]

texts = [
    "Уважаемая администрация, прошу обратить внимание на состояние дорог в нашем районе.",
    "Крик души! В поликлинике номер 5 огромные очереди, невозможно попасть к врачу.",
    "Примите меры! В школе не хватает учебников для учеников 7-х классов.",
    "От лица жителей обращаюсь по поводу отсутствия уличного освещения в нашем микрорайоне.",
    "Глава региона, прошу рассмотреть вопрос о строительстве новой детской площадки.",
    "Нет воды в доме номер 12 уже вторые сутки, прошу срочно разобраться!",
    "Погибли бездомные животные из-за отравления. Кто отвечает за это?",
    "Транспорт ходит с большими задержками, особенно в вечернее время.",
    "Прошу рассмотреть вопрос о ремонте крыши в многоквартирном доме на улице Центральная.",
    "Где обещанная модернизация больницы? Оборудование устарело, врачи не справляются."
]

comments_range = (0, 100)
shares_range = (0, 50)

data = []

for _ in range(500):
    topic = random.choice(topics)
    text = random.choice(texts)
    comments = random.randint(*comments_range)
    shares = random.randint(*shares_range)

    if random.random() < 0.05:
        comments = random.choice([150, 200, 300])
    if random.random() < 0.05:
        shares = random.choice([100, 150, 200])

    if random.random() < 0.08:
        text = None
    if random.random() < 0.05:
        topic = None

    data.append([topic, text, comments, shares])

df_texts = pd.DataFrame(data, columns=[
    "Тематика", "Текст обращения", "Количество комментариев", "Количество репостов"
])

file_path = "../data/text_data.csv"
df_texts.to_csv(file_path, index=False)

print("Текстовые данные успешно созданы!")